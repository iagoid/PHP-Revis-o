<?php
// Incremento
$valor = 20;

echo $valor++;
echo "<br>";
echo $valor;


echo "<hr>";

$valor = 20;

echo ++$valor;
echo "<br>";
echo $valor;
echo "<hr>";


// Decremento

$valor = 20;

echo $valor--;
echo "<br>";
echo $valor;


echo "<hr>";

$valor = 20;

echo --$valor;
echo "<br>";
echo $valor;

?>